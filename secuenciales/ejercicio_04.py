# Dados dos números muestra la suma.
# resta, multiplicación y divición.

num_1 = int(input("Ingresa un número: "))
num_2 = int(input("Ingresa otro número: "))

suma = num_1 + num_2
resta = num_1 - num_2
multi = num_1 * num_2
divi = num_1 / num_2

print("Sumas:", suma)
print("Resta:", resta)
print("Multiplicación:", multi)
print("División:", divi)